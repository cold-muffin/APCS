public class DataTypes {
	public static void main(String[] args) {
		// PRIMITIVE VARIABLES
		// Primitive variables hold "primitive types"
		// bool - 1 bit of memory
		// int - 32 bits of memory
		// double - 64 bits of memory
		int declaration;
		declaration = 100; // set value
		
		double set_value_in_declaration = 1.0;
		
		System.out.println(declaration);
		System.out.println(set_value_in_declaration + 5.5);
		
		// Concatenate strings together
		String name = "Efrat";
		
		System.out.println("Hello" + name);
		
		boolean cool = true;
		
		// OBJECT/REFERENCE VARIABLES
		// Object or reference variables hold references to an object of a class
	}
}